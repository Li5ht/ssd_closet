package com.ssp.closet.dto;

public class AccountRegister {
	private String username;
	private String userid;
	private String password;
	private String confirmPassword;
	private String addressNum;
	private String findAddress;
	private String postcode;
	private String roadAddress;
	private String parcelAddress;
	private String phone;
	private String email;
	
	public String getUsername() {return username;}
	public void setUsername(String username) {this.username = username;}
	
	public String getUserid() {return userid;}
	public void setUserid(String userid) {this.userid = userid;}
	
	public String getPassword() {return password;}
	public void setPassword(String password) {this.password = password;}
	
	public String getConfirmPassword() {return confirmPassword;}
	public void setConfirmPassword(String confirmPassword) {this.confirmPassword = confirmPassword;}
	
	public String getAddressNum() {return addressNum;}
	public void setAddressNum(String addressNum) {this.addressNum = addressNum;}
	
	public String getFindAddress() {return findAddress;}
	public void setFindAddress(String findAddress) {this.findAddress = findAddress;}
	
	public String getPostcode() {return postcode;}
	public void setPostcode(String postcode) {this.postcode = postcode;}
	
	public String getRoadAddress() {return roadAddress;}
	public void setRoadAddress(String roadAddress) {this.roadAddress = roadAddress;}
	
	public String getParcelAddress() {return parcelAddress;}
	public void setParcelAddress(String parcelAddress) {this.parcelAddress = parcelAddress;}
	
	public String getPhone() {return phone;}
	public void setPhone(String phone) {this.phone = phone;}
	
	public String getEmail() {return email;}
	public void setEmail(String email) {this.email = email;}

}
